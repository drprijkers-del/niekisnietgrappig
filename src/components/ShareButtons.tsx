"use client";

import { useState } from "react";
import { getUI, Lang } from "@/lib/content";
import { getSessionId } from "@/lib/session";

function getHomeUrl() {
  if (typeof window === "undefined") return "/";
  const hostname = window.location.hostname;
  if (hostname.match(/\.isnietgrappig\.com$/)) return "https://isnietgrappig.com";
  if (hostname.match(/\.isntfunny\.com$/)) return "https://isntfunny.com";
  return "/";
}

function getShareUrl(ref: string, groupId?: string) {
  const url = new URL(window.location.href);
  const hostname = url.hostname;
  const w = url.searchParams.get("w");
  const g = groupId || url.searchParams.get("g");

  const applyParams = (u: URL) => {
    u.searchParams.set("ref", ref);
    if (w) u.searchParams.set("w", w);
    if (g) u.searchParams.set("g", g);
  };

  // Already on a subdomain (dennis.isnietgrappig.com)
  const subNL = hostname.match(/^(.+)\.isnietgrappig\.com$/);
  const subEN = hostname.match(/^(.+)\.isntfunny\.com$/);
  const sub = subNL || subEN;
  if (sub && sub[1] !== "www") {
    const shareUrl = new URL(`https://${hostname}`);
    applyParams(shareUrl);
    return shareUrl.toString();
  }

  // Root domain with path-based name (isnietgrappig.com/dennis)
  const isNL = hostname === "isnietgrappig.com" || hostname === "www.isnietgrappig.com";
  const isEN = hostname === "isntfunny.com" || hostname === "www.isntfunny.com";
  if (isNL || isEN) {
    const naam = url.pathname.split("/").filter(Boolean)[0];
    if (naam) {
      const baseDomain = isNL ? "isnietgrappig.com" : "isntfunny.com";
      const shareUrl = new URL(`https://${decodeURIComponent(naam)}.${baseDomain}`);
      applyParams(shareUrl);
      return shareUrl.toString();
    }
  }

  // Fallback (localhost, vercel preview) â€” generate production subdomain URL
  const naam = url.pathname.split("/").filter(Boolean)[0];
  if (naam) {
    const shareUrl = new URL(`https://${decodeURIComponent(naam)}.isnietgrappig.com`);
    applyParams(shareUrl);
    return shareUrl.toString();
  }

  // Ultimate fallback
  url.searchParams.delete("ref");
  url.searchParams.set("ref", ref);
  return url.toString();
}

export default function ShareButtons({
  naam,
  lang,
  groupId,
}: {
  naam: string;
  lang: Lang;
  groupId?: string;
}) {
  const [copied, setCopied] = useState(false);
  const ui = getUI(lang).share;

  const trackShare = () => {
    const sid = getSessionId();
    const ttShare = Math.round(performance.now() / 1000);
    const g = groupId || (typeof window !== "undefined" ? new URL(window.location.href).searchParams.get("g") : null);
    fetch("/api/share", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({ naam, sid, ttShare, g }),
    }).catch(() => {});
  };

  const handleWhatsApp = () => {
    trackShare();
    const shareUrl = getShareUrl("wa", groupId);
    window.open(
      `https://wa.me/?text=${encodeURIComponent(`${ui.shareText(naam)} ${shareUrl}`)}`,
      "_blank"
    );
  };

  const handleCopy = async () => {
    trackShare();
    const shareUrl = getShareUrl("copy", groupId);
    const copyText = lang === "en"
      ? `${naam} is not funny and it's now official ðŸ˜‚ Check the evidence: ${shareUrl}`
      : `${naam} is niet grappig en het is nu officieel ðŸ˜‚ Check hier de feiten: ${shareUrl}`;
    try {
      await navigator.clipboard.writeText(copyText);
    } catch {
      const ta = document.createElement("textarea");
      ta.value = copyText;
      document.body.appendChild(ta);
      ta.select();
      document.execCommand("copy");
      document.body.removeChild(ta);
    }
    setCopied(true);
    setTimeout(() => setCopied(false), 2000);
  };

  return (
    <section className="border-t border-zinc-800 bg-zinc-950/50 py-20 px-6">
      <div className="mx-auto max-w-md text-center">
        <h2 className="text-2xl font-bold sm:text-3xl">{ui.heading}</h2>
        <p className="mt-4 text-zinc-400">{ui.description}</p>
        <div className="mt-8 flex flex-col gap-3">
          <button
            onClick={handleWhatsApp}
            className="flex w-full items-center justify-center gap-2 rounded-full bg-red-600 px-6 py-3 font-medium text-white transition-all hover:bg-red-500"
          >
            <svg width="18" height="18" viewBox="0 0 24 24" fill="currentColor">
              <path d="M17.472 14.382c-.297-.149-1.758-.867-2.03-.967-.273-.099-.471-.148-.67.15-.197.297-.767.966-.94 1.164-.173.199-.347.223-.644.075-.297-.15-1.255-.463-2.39-1.475-.883-.788-1.48-1.761-1.653-2.059-.173-.297-.018-.458.13-.606.134-.133.298-.347.446-.52.149-.174.198-.298.298-.497.099-.198.05-.371-.025-.52-.075-.149-.669-1.612-.916-2.207-.242-.579-.487-.5-.669-.51-.173-.008-.371-.01-.57-.01-.198 0-.52.074-.792.372-.272.297-1.04 1.016-1.04 2.479 0 1.462 1.065 2.875 1.213 3.074.149.198 2.096 3.2 5.077 4.487.709.306 1.262.489 1.694.625.712.227 1.36.195 1.871.118.571-.085 1.758-.719 2.006-1.413.248-.694.248-1.289.173-1.413-.074-.124-.272-.198-.57-.347m-5.421 7.403h-.004a9.87 9.87 0 01-5.031-1.378l-.361-.214-3.741.982.998-3.648-.235-.374a9.86 9.86 0 01-1.51-5.26c.001-5.45 4.436-9.884 9.888-9.884 2.64 0 5.122 1.03 6.988 2.898a9.825 9.825 0 012.893 6.994c-.003 5.45-4.437 9.884-9.885 9.884m8.413-18.297A11.815 11.815 0 0012.05 0C5.495 0 .16 5.335.157 11.892c0 2.096.547 4.142 1.588 5.945L.057 24l6.305-1.654a11.882 11.882 0 005.683 1.448h.005c6.554 0 11.89-5.335 11.893-11.893a11.821 11.821 0 00-3.48-8.413z" />
            </svg>
            {ui.shareButton()}
          </button>
          <button
            onClick={handleCopy}
            className="flex w-full items-center justify-center gap-2 rounded-full border border-zinc-700 px-6 py-3 font-medium transition-all hover:bg-white hover:text-black hover:border-white"
          >
            {copied ? (
              <>
                <svg width="16" height="16" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round">
                  <polyline points="20 6 9 17 4 12" />
                </svg>
                {ui.copied}
              </>
            ) : (
              <>
                <svg width="16" height="16" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round">
                  <path d="M10 13a5 5 0 0 0 7.54.54l3-3a5 5 0 0 0-7.07-7.07l-1.72 1.71" />
                  <path d="M14 11a5 5 0 0 0-7.54-.54l-3 3a5 5 0 0 0 7.07 7.07l1.71-1.71" />
                </svg>
                {ui.copyLink}
              </>
            )}
          </button>
          <button
            onClick={() => { window.location.href = getHomeUrl(); }}
            className="flex w-full items-center justify-center gap-2 rounded-full border border-zinc-700 px-6 py-3 font-medium transition-all hover:bg-white hover:text-black hover:border-white"
          >
            <svg width="16" height="16" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round">
              <line x1="12" y1="5" x2="12" y2="19" />
              <line x1="5" y1="12" x2="19" y2="12" />
            </svg>
            {ui.create}
          </button>
        </div>
      </div>
    </section>
  );
}
